package escritoriofastpacket.modelo.pojo;

/**
 *
 * @author uriel
 */
public class Unidad {
    
    private Integer idUnidad;
    private String marca;
    private String modelo;
    private String anio;
    private String vin;
    private String noIdentificacion;
    private Integer idTipoUnidad;
    private String tipoUnidad;
    private String motivo;

    public Unidad() {
    }

    public Unidad(Integer idUnidad, String marca, String modelo, String anio, String vin, String noIdentificacion, Integer idTipoUnidad, String tipoUnidad, String motivo) {
        this.idUnidad = idUnidad;
        this.marca = marca;
        this.modelo = modelo;
        this.anio = anio;
        this.vin = vin;
        this.noIdentificacion = noIdentificacion;
        this.idTipoUnidad = idTipoUnidad;
        this.tipoUnidad = tipoUnidad;
        this.motivo = motivo;
    }

    public Integer getIdUnidad() {
        return idUnidad;
    }

    public void setIdUnidad(Integer idUnidad) {
        this.idUnidad = idUnidad;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getAnio() {
        return anio;
    }

    public void setAnio(String anio) {
        this.anio = anio;
    }

    public String getVin() {
        return vin;
    }

    public void setVin(String vin) {
        this.vin = vin;
    }

    public String getNoIdentificacion() {
        return noIdentificacion;
    }

    public void setNoIdentificacion(String noIdentificacion) {
        this.noIdentificacion = noIdentificacion;
    }

    public Integer getIdTipoUnidad() {
        return idTipoUnidad;
    }

    public void setIdTipoUnidad(Integer idTipoUnidad) {
        this.idTipoUnidad = idTipoUnidad;
    }

    public String getTipoUnidad() {
        return tipoUnidad;
    }

    public void setTipoUnidad(String tipoUnidad) {
        this.tipoUnidad = tipoUnidad;
    }

    public String getMotivo() {
        return motivo;
    }

    public void setMotivo(String motivo) {
        this.motivo = motivo;
    }

    
    
}
